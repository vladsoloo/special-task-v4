card_values = {
    6: 'Шестерка',
    7: 'Семерка',
    8: 'Восьмерка',
    9: 'Девятка',
    10: 'Десятка',
    11: 'Валет',
    12: 'Дама',
    13: 'Король',
    14: 'Туз'
}


k = int(input('Введите номер карты (от 6 до 14): '))


if 6 <= k <= 14:

    card_name = card_values[k]
    print(f'Достоинство карты: {card_name}')
else:
    print('Ошибка: недопустимый номер карты.')
